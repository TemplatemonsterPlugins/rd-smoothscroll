<h2 class="item1">Options</h2>
<p class="intro">
    There are several options available in the script:
</p>

<h3>Step</h3>

<dl>
    <dt>Type:</dt>
    <dd>Number</dd>
</dl>
<dl>
    <dt>Default value:</dt>
    <dd>2</dd>
</dl>

<p>
    Parameter sets the speed multiplier of element's scroll.
    Use it to adjust scrolling speed.
</p>

<h3>Friction</h3>

<dl>
    <dt>Type:</dt>
    <dd>Number</dd>
</dl>
<dl>
    <dt>Default value:</dt>
    <dd>0.95</dd>
</dl>

<p>
    Parameter sets the speed deceleration multiplier of element's scroll.
    Use it to adjust scroll friction.
</p>

<h3>MinDistance</h3>

<dl>
    <dt>Type:</dt>
    <dd>Number</dd>
</dl>
<dl>
    <dt>Default value:</dt>
    <dd>0.1</dd>
</dl>

<p>
    Parameter sets the minimal distance for scroll to animate.
</p>