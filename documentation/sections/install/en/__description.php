<h2 class="item1">Initialization</h2>
<p class="intro">
    The process of script integration is simple and consists from several steps:
</p>

<h3>Download script from Git</h3>

<p>
    First of all, you need to download the script from our public repo:
    <a href='http://products.git.devoffice.com/html-coding/rd-smoothscroll'>Click</a>
</p>

<h3>Include script on page</h3>

<p>
    Copy script into your project and include it on the page. Use this code:
</p>

<code>
    &lt;script src="js/rd-smoothscroll.min.js"&gt;&lt;/script&gt;
</code>

<p>
    Script has an auto initialize feature, so that's no need to initialize it manually.
</p>